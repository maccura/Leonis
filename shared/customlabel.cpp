CustomLabel::CustomLabel(QWidget* parent) : QLabel(parent)
{

}

///
/// @brief
///     设置文本
///
/// @param[in]  strText  文本
///
/// @return 
///
/// @par History:
/// @li 4170/TangChuXian，2023年8月4日，新建函数
///
void CustomLabel::setText(const QString& strText)
{
    // 基类处理
    QLabel::setText(strText);

    // 发送信号
    emit textChanged(strText);
}

